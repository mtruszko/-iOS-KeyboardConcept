import UIKit

extension UIColor {
    
    class func keyColor() -> UIColor {
        
        return UIColor.init(red: 69/255, green: 63/255, blue: 100/255, alpha: 1)
    }
    
    class func keySelectedColor() -> UIColor {
        
        return UIColor.init(red: 128/255, green: 121/255, blue: 164/255, alpha: 1)
    }
    
    class func keySelectedSecondColor() -> UIColor {
        
        return UIColor.init(red: 159/255, green: 77/255, blue: 44/255, alpha: 1)
    }
    
    class func backgroundColor() -> UIColor {
        
        return UIColor.init(red: 238/255, green: 238/255, blue: 238/255, alpha: 1)
    }
    
    class func circleColor() -> UIColor {
        
        return UIColor.init(red: 74/255, green: 21/255, blue: 101/255, alpha: 1)
    }
    
    
}